    private void compilar() {
        try{
            dni = Dni.getText();
            tipo = "dni";
            validador();
            
            nombre = Nombre.getText();
            tipo = "nombre";
            validador();
            
            apellidos = Apellidos.getText();
            tipo = "apellidos";
            validador();
                
            sexo = Nombre.getText().toLowerCase();
            tipo = "sexo";
            validador();
            
            edad = Integer.parseInt(Edad.getText());
            tipo = "edad";
            validador();
            
            peso = Double.parseDouble(Peso.getText());
            tipo = "peso";
            validador();
            
            Ejercicio19.crearClase(dni, nombre, apellidos, sexo, edad, peso);
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "dato no valido");
            Ejercicio19.vaciar();
        }
    }

    private void validador() throws Exception {
        switch (tipo){
            case "dni":mat = pat.matcher(dni);
            if (mat.matches()==false)
                throw new datoSinSentido();
                   
            case "nombre": mat = pat2.matcher(nombre);
            if (mat.matches()==false)
                throw new datoSinSentido();
            
            case "apellidos": mat = pat3.matcher(apellidos);
            if (mat.matches()==false)
                throw new datoSinSentido();
            
            case "sexo": mat = pat4.matcher(sexo);
            if (mat.matches()==false)
                throw new datoSinSentido();
            
            case "edad": mat = pat5.matcher(edad.toString());
            if (mat.matches()==false)
                throw new datoSinSentido();
            
            case "peso": mat = pat6.matcher(peso.toString());
            if (mat.matches()==false)
                throw new datoSinSentido();
        }  
    }
